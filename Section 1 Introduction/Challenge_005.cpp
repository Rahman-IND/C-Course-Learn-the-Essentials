/*Challenge: Create a program to assign values to double and char variables.
1.  Create a double variable named number with value 78.34.
2.  Create a character variable named alphabet with value 'u'.
3.  Print both variables in separate lines.*/

#include<iostream>
using namespace std;
int main(){
    //  creating double type variable
    double number = 78.34;
    //  creating char type variable
    char alphabet = 'u';

    //  printing number and alphabet variables
    cout<<number<<endl;
    cout<<alphabet;
    return 0;
}