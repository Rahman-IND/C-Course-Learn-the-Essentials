/*Topic: Changing Values Stored in Variables*/

#include<iostream>
using namespace std;
int main(){
    //  create int Age variable with value 25;
    int Age = 25;
    //  print Age
    cout<<Age<<endl;

    //  change value of Age variable from 25 to 19.
    Age = 19;
    //  print Age
    cout<<Age;
    return 0;
}